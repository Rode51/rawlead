def reply_retry_user_suffix(*, reason: str, attempt: int, layer: str) -> str:
    if "similar" in reason:
        return (
            f"\n\n[retry {attempt} {layer}: слишком похож на исходник — "
            "другой каркас, не синонимы. "
            "Выбери другую структуру: вопрос первым (B), или начни с опыта (A), или суть→детали→вопрос (C).]"
        )
    if "ai_smell" in reason or "triple_snachala" in reason:
        return (
            f"\n\n[retry {attempt} {layer}: пиши проще, как фрилансер в чате FL; "
            "убери канцелярит и лишний техжаргон не из ТЗ.]"
        )
    if "attachment_claim" in reason or "attachment_content" in reason:
        return (
            f"\n\n[retry {attempt} {layer}: текста ТЗ из файла нет — "
            "убери «вижу ZIP/архив/готовую вёрстку» и описание содержимого; "
            "если файл на странице, но не прочитан — «ознакомлюсь в диалоге»; "
            "если заказчик обещает архив — «когда будет» или попроси выслать.]"
        )
    return f"\n\n[retry {attempt} {layer}: перепиши заново, другой голос.]"


def l3_retry_user_suffix(*, reason: str, attempt: int) -> str:
    return reply_retry_user_suffix(reason=reason, attempt=attempt, layer="L3")
