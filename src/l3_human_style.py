def _token_set(text: str) -> set[str]:
    words = re.findall(r"[а-яёa-z0-9]{3,}", (text or "").casefold())
    return {w for w in words if w not in _STOPWORDS}


def _sentence_skeletons(text: str) -> list[str]:
    parts = re.split(r"(?<=[.!?…])\s+", (text or "").strip())
    out: list[str] = []
    for p in parts:
        p = p.strip()
        if not p:
            continue
        sk = re.sub(
            r"\b(выполню|разработаю|создам|настрою|сделаю|реализую|подготовлю|"
            r"сначала|затем|далее|потом|уточните|подскажите)\b",
            "_",
            p.casefold(),
        )
        sk = re.sub(r"\s+", " ", sk).strip()
        if len(sk) > 12:
            out.append(sk)
    return out


def l3_too_similar(
    shared: str, personal: str, *, min_ratio: float = _L3_SIMILAR_MIN_RATIO
) -> bool:
    a = re.sub(r"\s+", " ", (shared or "").strip().casefold())
    b = re.sub(r"\s+", " ", (personal or "").strip().casefold())
    if not a or not b:
        return True
    if a == b or (len(a) > 50 and (a in b or b in a)):
        return True

    ta, tb = _token_set(a), _token_set(b)
    if ta and tb:
        overlap = len(ta & tb) / max(len(ta), len(tb))
        if overlap >= min_ratio:
            sk_a, sk_b = _sentence_skeletons(shared), _sentence_skeletons(personal)
            if sk_a and sk_b and len(sk_a) == len(sk_b):
                same_slots = sum(1 for x, y in zip(sk_a, sk_b) if x == y)
                if same_slots / len(sk_a) >= 0.6:
                    return True
            if overlap >= _L3_SIMILAR_HARD_OVERLAP:
                return True

    def _canon(text: str) -> str:
        out = text
        for w1, w2 in _COSMETIC_PAIRS:
            out = re.sub(rf"\b{re.escape(w2)}\b", w1, out)
            out = re.sub(rf"\b{re.escape(w1)}\b", w1, out)
        return re.sub(r"\s+", " ", out).strip()

    if _canon(a) == _canon(b):
        return True
    ca, cb = _token_set(_canon(a)), _token_set(_canon(b))
    if ca and cb and len(ca & cb) / max(len(ca), len(cb)) >= _L3_SIMILAR_CANON_OVERLAP:
        return True
    return False


